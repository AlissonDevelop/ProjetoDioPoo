public abstract class Conteudo {

    private String item;
    private String descricao;


    public String getItem() {
        return item;
    }

    public void setItem(String Item) {
        this.item = Item;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
}